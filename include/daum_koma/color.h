#ifndef _COLOR_H
#define _COLOR_H 1

/* ansi terminal coloring */
#define CLEAR       "\e[0m"
#define RESET       "\e[0m"
#define BOLD        "\e[1m"
#define DARK        "\e[2m"
#define UNDERLINE   "\e[4m"
#define UNDERSCORE  "\e[4m"
#define BLINK       "\e[5m"
#define REVERSE     "\e[7m"
#define CONCEALED   "\e[8m"
#define BLACK       "\e[30m"
#define RED         "\e[31m"
#define GREEN       "\e[32m"
#define YELLOW      "\e[33m"
#define BLUE        "\e[34m"
#define MAGENTA     "\e[35m"
#define CYAN        "\e[36m"
#define WHITE       "\e[37m"
#define ON_BLACK    "\e[40m"
#define ON_RED      "\e[41m"
#define ON_GREEN    "\e[42m"
#define ON_YELLOW   "\e[43m"
#define ON_BLUE     "\e[44m"
#define ON_MAGENTA  "\e[45m"
#define ON_CYAN     "\e[46m"
#define ON_WHITE    "\e[47m"

#endif
